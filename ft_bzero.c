#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*pasta;

	i = 0;
	pasta = s;
	while (i < n)
	{
		pasta[i] = '\0';
		i++;
	}
	s = pasta;
}
